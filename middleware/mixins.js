export default {
    methods: {
        plus(a, b) {
            return a + b
        },
        minus(a, b) {
            return a - b
        },
        proiz(a, b) {
            return a * b
        },
        sqrt(a, b) {
            return a + 2 * b
        },
        del(a, b) {
            return a / b
        },
    }
}